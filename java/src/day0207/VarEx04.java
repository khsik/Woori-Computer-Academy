package day0207;

class VarEx04 {
	public static void main(String[] args) {
		// char 타입
		// 'A' = 65 , 'a' = 97 , '0' = 48
		char c1 = 'A';
		System.out.println(c1);		// 문자 출력
		System.out.println((int)c1);	// 문자에 해당하는 정수값 출력

		char c2 = 66;				// 정수 값 대입
		System.out.println(c2);		// 정수 값에 해당하는 문자 출력

		char c3 = 67;
		System.out.println(c3);
		System.out.println((int)c3);
	}
}