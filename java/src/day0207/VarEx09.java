package day0207;

class VarEx09 {
	public static void main(String[] args) {
		// 논리형 boolean - true/false의 값만 들어감
		boolean a;	// 변수 선언
		a = true;	// 값 대입
		System.out.println(a);

		boolean b;
		b = false;
		System.out.println(b);

		int c = 5;
		int d = 10;
		int result = c+d;

		boolean result2 = c>d;
		System.out.println(result2);
	}
}